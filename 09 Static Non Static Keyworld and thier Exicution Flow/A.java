//modularity
// modularity means dividing a big task into a small pieces
// in java we can achieve modularity.
//methods and class with classes.

class A {
	static void add(int a,int b){
		//addition
		System.out.println(a+b);
	}
	static void sub(int a,int b){
		//Substraction
		System.out.println(a-b);
	}
	static void mul(int a ,int b){
		//multiplication
		System.out.println(a*b);
	}
	static void div(int a, int b){
		//div
		System.out.println(a/b);
	}

}
